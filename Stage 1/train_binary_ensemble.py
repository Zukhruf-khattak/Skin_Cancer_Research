    sample_images = []
    sample_labels = []
    for images, labels in test_loader:
        for img, lbl in zip(images, labels):
            if int(lbl) == 1: # Malignant
                sample_images.append(img)
                sample_labels.append(int(lbl))
            if len(sample_images) >= 4:
                break
        if len(sample_images) >= 4:
            break
            
    fig, axes = plt.subplots(2, 4, figsize=(14, 8))
    gcam = GradCAM(top_model, target_layer)
    
    for idx, (img_tensor, lbl) in enumerate(zip(sample_images, sample_labels)):
        # Denormalize image for correct display
        orig_img = denormalize(img_tensor)
        
        # Generate Grad-CAM activation map
        cam_map = gcam.generate(img_tensor.unsqueeze(0).to(device), class_idx=1)
        
        # Resize cam_map to input size (224, 224)
        cam_resized = np.array(Image.fromarray(cam_map).resize((224, 224), Image.Resampling.BILINEAR))
        
        # Overlay heatmap on original image
        heatmap = cm.jet(cam_resized)[:, :, :3]
        overlay = (0.55 * orig_img + 0.45 * heatmap).clip(0, 1)
        
        # Predict class & probability for displaying
        top_model.eval()
        with torch.no_grad():
            with autocast(enabled=True):
                logit = top_model(img_tensor.unsqueeze(0).to(device))
                prob = torch.softmax(logit, dim=-1)[0, 1].item()
                
        pred_lbl = "Malignant" if prob >= clinical_threshold else "Benign"
        correct = "✓" if pred_lbl == "Malignant" else "✗"
        
        # Plot Original Image
        axes[0, idx].imshow(orig_img)
        axes[0, idx].set_title(f"Original test {idx+1}\nTrue: Malignant", fontsize=10)
        axes[0, idx].axis('off')
        
        # Plot Grad-CAM Overlay
        axes[1, idx].imshow(overlay)
        axes[1, idx].set_title(f"Grad-CAM ({best_cnn_name})\nP(Mal)={prob:.2f} → {pred_lbl} {correct}", fontsize=9)
        axes[1, idx].axis('off')
        
    gcam.remove_hooks()
    
    plt.suptitle(f"Grad-CAM Heatmaps using Top CNN Model: {best_cnn_name}", fontsize=14, fontweight='bold', y=0.98)
    plt.tight_layout()
    plt.savefig(save_dir / 'grad_cam.png', dpi=300, bbox_inches='tight')
    plt.close()
