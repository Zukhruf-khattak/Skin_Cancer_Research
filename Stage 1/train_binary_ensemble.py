# 2. CUSTOM LOSS FUNCTION
# ==========================================

class WeightedSoftCELoss(nn.Module):
    """
    Custom Cross-Entropy Loss that supports class-weighting, label smoothing,
    and handles both integer labels and soft labels (generated by CutMix/MixUp).
    """
    def __init__(self, weight=None, label_smoothing=0.1):
        super().__init__()
        self.weight = weight
        self.label_smoothing = label_smoothing

    def forward(self, logits, targets):
        num_classes = logits.size(-1)
        log_probs = F.log_softmax(logits, dim=-1)

        # Handle standard integer labels
        if targets.ndim == 1:
            smooth_targets = torch.full_like(log_probs, self.label_smoothing / num_classes)
            smooth_targets.scatter_(1, targets.unsqueeze(1), 1.0 - self.label_smoothing + (self.label_smoothing / num_classes))
        # Handle soft targets (CutMix/MixUp outputs)
        else:
            smooth_targets = targets * (1.0 - self.label_smoothing) + self.label_smoothing / num_classes

        # Apply class weights
        if self.weight is not None:
            weighted_log_probs = log_probs * self.weight.unsqueeze(0)
            loss = -(smooth_targets * weighted_log_probs).sum(dim=-1).mean()
        else:
            loss = -(smooth_targets * log_probs).sum(dim=-1).mean()
        return loss

