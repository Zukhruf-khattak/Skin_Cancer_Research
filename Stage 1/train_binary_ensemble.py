# 6. DENORMALIZATION
# ==========================================

DENORM_MEAN = torch.tensor([0.485, 0.456, 0.406])
DENORM_STD = torch.tensor([0.229, 0.224, 0.225])

def denormalize(tensor):
    """
    Reverse ImageNet normalization for displaying original images.
    """
    t = tensor.clone().cpu()
    t = t * DENORM_STD[:, None, None] + DENORM_MEAN[:, None, None]
    return t.permute(1, 2, 0).numpy().clip(0, 1)

