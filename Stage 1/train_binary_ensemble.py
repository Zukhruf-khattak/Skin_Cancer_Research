class GradCAM:
    """
    Hook-based Grad-CAM implementation for CNN architectures.
    """
    def __init__(self, model, target_layer):
        self.model = model
        self.target_layer = target_layer
        self.activations = None
        self.gradients = None
        self.handlers = []
        
        # Register hooks
        self.handlers.append(target_layer.register_forward_hook(self._forward_hook))
        self.handlers.append(target_layer.register_full_backward_hook(self._backward_hook))
        
    def _forward_hook(self, module, input, output):
        self.activations = output.detach()
        
    def _backward_hook(self, module, grad_input, grad_output):
        self.gradients = grad_output[0].detach()
        
    def generate(self, input_tensor, class_idx=1):
        self.model.zero_grad()
        output = self.model(input_tensor)
        
        loss = output[0, class_idx]
        loss.backward()
        
        grads = self.gradients[0]
        acts = self.activations[0]
        
        # Global average pooling of gradients
        weights = torch.mean(grads, dim=[1, 2])
        
        # Weighted combination of activations
        cam = torch.zeros(acts.shape[1:], dtype=torch.float32, device=acts.device)
        for i, w in enumerate(weights):
            cam += w * acts[i, :, :]
            
        cam = F.relu(cam)
        cam = cam.cpu().numpy()
        
        # Min-max normalization
        cam_min, cam_max = cam.min(), cam.max()
        if cam_max > cam_min:
            cam = (cam - cam_min) / (cam_max - cam_min)
        else:
            cam = np.zeros_like(cam)
            
        return cam
        
    def remove_hooks(self):
        for h in self.handlers:
            h.remove()

def get_gradcam_layer(model_name, model):
    """
    Helper function to dynamically identify and return the target convolutional
    layer for Grad-CAM.
    """
    if model_name == 'ResNet50':
        return model.layer4[-1]
    elif model_name == 'EfficientNet-B0':
        return model.features[-1]
    elif model_name == 'MobileNetV2':
        return model.features[-1]
    elif model_name == 'ConvNeXt-Tiny':
        return model.features[-1][-1]
    return None

