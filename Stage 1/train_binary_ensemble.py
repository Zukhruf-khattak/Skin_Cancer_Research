def run_pipeline(args):
    # Setup directories
    save_dir = Path(args.save_dir)
    save_dir.mkdir(parents=True, exist_ok=True)
    
    device = torch.device(args.device if torch.cuda.is_available() else 'cpu')
    print(f"Using device: {device}")
    
    # ── Preprocessing & Data Loading ──
    print("\nLoading data...")
    df_gt = pd.read_csv(args.csv_path)
    
    # Class mapping: Malignant (MEL, BCC, AK, SCC) vs Benign (NV, BKL, DF, VASC)
    MALIGNANT_COLS = ['MEL', 'BCC', 'AK', 'SCC']
    df_gt['label'] = 0
    for col in MALIGNANT_COLS:
        if col in df_gt.columns:
            df_gt.loc[df_gt[col] == 1, 'label'] = 1
            
    # Load metadata if exists for GroupShuffleSplit
    metadata_path = Path(args.meta_path)
    if metadata_path.exists():
        df_meta = pd.read_csv(metadata_path)
        df = pd.merge(df_gt, df_meta, on='image', how='inner')
        print(f"Metadata loaded. Total rows: {len(df)}")
    else:
        df = df_gt.copy()
        print("Metadata file not found. Falling back to GroundTruth only.")
        
    # Strict 70/15/15 split using GroupShuffleSplit if lesion_id exists
    if 'lesion_id' in df.columns:
        print("Performing GroupShuffleSplit using 'lesion_id' to prevent data leakage...")
        gss1 = GroupShuffleSplit(n_splits=1, test_size=0.15, random_state=42)
        train_full_idx, test_idx = next(gss1.split(df, groups=df['lesion_id']))
        df_train_full = df.iloc[train_full_idx].reset_index(drop=True)
        df_test = df.iloc[test_idx].reset_index(drop=True)
        
        # Split train_full into train (70%) and val (15%)
        val_rel_size = 0.15 / 0.85
        gss2 = GroupShuffleSplit(n_splits=1, test_size=val_rel_size, random_state=42)
        train_idx, val_idx = next(gss2.split(df_train_full, groups=df_train_full['lesion_id']))
        df_train_raw = df_train_full.iloc[train_idx].reset_index(drop=True)
        df_val = df_train_full.iloc[val_idx].reset_index(drop=True)
    else:
        print("Performing standard stratified Train/Validation/Test split...")
        df_train_full, df_test = train_test_split(df, test_size=0.15, random_state=42, stratify=df['label'])
        df_train_raw, df_val = train_test_split(df_train_full, test_size=0.1765, random_state=42, stratify=df_train_full['label'])
        
    print(f"Original splits:")
    print(f"  Train (raw): {len(df_train_raw)} (Malignant: {(df_train_raw['label'] == 1).sum()}, Benign: {(df_train_raw['label'] == 0).sum()})")
    print(f"  Val:         {len(df_val)} (Malignant: {(df_val['label'] == 1).sum()}, Benign: {(df_val['label'] == 0).sum()})")
    print(f"  Test:        {len(df_test)} (Malignant: {(df_test['label'] == 1).sum()}, Benign: {(df_test['label'] == 0).sum()})")
    
    # Undersample Benign in training set to achieve 50:50 ratio
    df_train_benign = df_train_raw[df_train_raw['label'] == 0]
    df_train_malignant = df_train_raw[df_train_raw['label'] == 1]
    
    n_malignant = len(df_train_malignant)
    df_train_benign_under = df_train_benign.sample(n=n_malignant, random_state=42)
    df_train = pd.concat([df_train_benign_under, df_train_malignant]).sample(frac=1, random_state=42).reset_index(drop=True)
    
    print(f"\nTraining set after 50:50 undersampling:")
    print(f"  Total: {len(df_train)} (Malignant: {(df_train['label'] == 1).sum()}, Benign: {(df_train['label'] == 0).sum()})")
    
    # ── PyTorch v2 Transforms ──
    train_transforms = v2.Compose([
        v2.ToImage(),
        v2.Resize((224, 224), antialias=True),
        v2.RandomHorizontalFlip(p=0.5),
        v2.RandomVerticalFlip(p=0.5),
        v2.RandomRotation(degrees=15),
        v2.ColorJitter(brightness=0.2, contrast=0.2, saturation=0.2, hue=0.1),
        v2.RandAugment(num_ops=2, magnitude=9),
        v2.ToDtype(torch.float32, scale=True),
        v2.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
    ])
    
    val_test_transforms = v2.Compose([
        v2.ToImage(),
        v2.Resize((224, 224), antialias=True),
        v2.ToDtype(torch.float32, scale=True),
        v2.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225])
    ])
    
    # Mixup and Cutmix loaders
    cutmix = v2.CutMix(num_classes=2)
    mixup = v2.MixUp(num_classes=2, alpha=0.4)
    cutmix_or_mixup = v2.RandomChoice([cutmix, mixup])
    
    train_dataset = ISICDataset(df_train, args.img_dir, train_transforms)
    val_dataset = ISICDataset(df_val, args.img_dir, val_test_transforms)
    test_dataset = ISICDataset(df_test, args.img_dir, val_test_transforms)
    
    train_loader = DataLoader(train_dataset, batch_size=args.batch_size, shuffle=True, num_workers=args.num_workers, pin_memory=True)
    val_loader = DataLoader(val_dataset, batch_size=args.batch_size, shuffle=False, num_workers=args.num_workers, pin_memory=True)
    test_loader = DataLoader(test_dataset, batch_size=args.batch_size, shuffle=False, num_workers=args.num_workers, pin_memory=True)
    
    # ── Augmentation Grid ──
    print("\nGenerating Preprocessing Augmentation Grid...")
    fig, axes = plt.subplots(4, 4, figsize=(12, 12))
    
    # Draw a sample image and apply augmentations manually for display
    raw_sample_df = df_train.head(4)
    for idx, (_, row) in enumerate(raw_sample_df.iterrows()):
        img_name = row['image']
        label = row['label']
        for ext in ['.jpg', '.jpeg', '.png', '.JPG']:
            path = Path(args.img_dir) / (img_name + ext)
            if path.exists():
                break
        img_pil = Image.open(path).convert('RGB')
        img_resized = img_pil.resize((224, 224))
        
        # Column 0: Original
        axes[idx, 0].imshow(img_resized)
        axes[idx, 0].set_title(f"Original ({'Mal' if label==1 else 'Ben'})", fontsize=9)
        axes[idx, 0].axis('off')
        
        # Column 1: RandAugment
        img_aug_tensor = train_transforms(img_pil)
        img_aug_np = denormalize(img_aug_tensor)
        axes[idx, 1].imshow(img_aug_np)
        axes[idx, 1].set_title("RandAugment", fontsize=9)
        axes[idx, 1].axis('off')
        
    # Get a batch of images to generate CutMix and MixUp
    images_batch, labels_batch = next(iter(DataLoader(train_dataset, batch_size=4, shuffle=True)))
    
    # Apply CutMix
    images_cutmix, labels_cutmix = cutmix(images_batch.clone(), labels_batch.clone())
    # Apply MixUp
    images_mixup, labels_mixup = mixup(images_batch.clone(), labels_batch.clone())
    
    for idx in range(4):
        # Column 2: CutMix
        axes[idx, 2].imshow(denormalize(images_cutmix[idx]))
        axes[idx, 2].set_title("CutMix Batch", fontsize=9)
        axes[idx, 2].axis('off')
        
        # Column 3: MixUp
        axes[idx, 3].imshow(denormalize(images_mixup[idx]))
        axes[idx, 3].set_title("MixUp Batch", fontsize=9)
        axes[idx, 3].axis('off')
        
    plt.suptitle("ISIC 2019 — Preprocessing and Augmentation Pipeline", fontsize=14, fontweight='bold', y=0.98)
    plt.tight_layout()
    plt.savefig(save_dir / 'augmentation_grid.png', dpi=300, bbox_inches='tight')
    plt.close()
    print(f"Augmentation grid saved to {save_dir / 'augmentation_grid.png'}")
    
    # ── Class-weighted Loss & Setup ──
    # Compute class weights based on the undersampled training split (which is 50:50, so weights will be equal 1.0,
    # but we write the computation dynamically for completeness/reproducibility)
    n_b = (df_train['label'] == 0).sum()
    n_m = (df_train['label'] == 1).sum()
    total_train = len(df_train)
    
    w_b = total_train / (2 * n_b)
    w_m = total_train / (2 * n_m)
    class_weights = torch.tensor([w_b, w_m], dtype=torch.float).to(device)
    print(f"\nClass weights (Benign vs Malignant): {class_weights.cpu().numpy()}")
    
    criterion = WeightedSoftCELoss(weight=class_weights, label_smoothing=0.1)
    
    # ── Train All 5 Models ──
    models_to_train = ['ResNet50', 'EfficientNet-B0', 'MobileNetV2', 'ConvNeXt-Tiny', 'Swin-Tiny']
    trained_models = {}
    histories = {}
    
    for model_name in models_to_train:
        print(f"\n==========================================")
        print(f" Training Model: {model_name}")
        print(f"==========================================")
        
        model = get_model(model_name).to(device)
        
        # LR setup (Swin-Tiny needs smaller LR, Swin weight decay is typically larger)
        if model_name == 'Swin-Tiny':
            lr = 5e-5
            weight_decay = 0.01
        else:
            lr = 1e-4
            weight_decay = 1e-4
            
        optimizer = optim.AdamW(model.parameters(), lr=lr, weight_decay=weight_decay)
        scheduler = CosineAnnealingLR(optimizer, T_max=args.epochs, eta_min=1e-6)
        scaler = GradScaler(enabled=True) # AMP GradScaler
        
        best_val_loss = float('inf')
        best_model_wts = copy.deepcopy(model.state_dict())
        patience_counter = 0
        
        history = {'train_loss': [], 'val_loss': [], 'train_acc': [], 'val_acc': []}
        
        for epoch in range(1, args.epochs + 1):
            # ── Training Loop ──
            model.train()
            running_loss = 0.0
            corrects = 0
            total = 0
            
            for images, labels in train_loader:
                images = images.to(device)
                labels = labels.to(device)
                
                # Apply CutMix/MixUp dynamically with 50% probability
                if random.random() < 0.5:
                    images, targets = cutmix_or_mixup(images, labels)
                else:
                    targets = F.one_hot(labels, num_classes=2).float()
                    
                optimizer.zero_grad()
                
                # AMP Autocast
                with autocast(enabled=True):
                    outputs = model(images)
                    loss = criterion(outputs, targets)
                    
                scaler.scale(loss).backward()
                scaler.step(optimizer)
                scaler.update()
                
                running_loss += loss.item() * images.size(0)
                preds = outputs.argmax(dim=-1)
                
                # Correct count computed against standard label
                corrects += (preds == labels).sum().item()
                total += labels.size(0)
                
            train_loss = running_loss / total
            train_acc = corrects / total
            
            # ── Validation Loop ──
            model.eval()
            val_loss_sum = 0.0
            val_corrects = 0
            val_total = 0
            
            with torch.no_grad():
                for images, labels in val_loader:
                    images = images.to(device)
                    labels = labels.to(device)
                    
                    with autocast(enabled=True):
                        outputs = model(images)
                        loss = criterion(outputs, labels)
                        
                    val_loss_sum += loss.item() * images.size(0)
                    preds = outputs.argmax(dim=-1)
                    val_corrects += (preds == labels).sum().item()
                    val_total += labels.size(0)
                    
            val_loss = val_loss_sum / val_total
            val_acc = val_corrects / val_total
            
            scheduler.step()
            
            history['train_loss'].append(train_loss)
            history['val_loss'].append(val_loss)
            history['train_acc'].append(train_acc)
            history['val_acc'].append(val_acc)
            
            print(f"Epoch {epoch}/{args.epochs} — Train Loss: {train_loss:.4f} Acc: {train_acc:.4f} | Val Loss: {val_loss:.4f} Acc: {val_acc:.4f}")
            
            # Early stopping on validation loss
            if val_loss < best_val_loss:
                best_val_loss = val_loss
                best_model_wts = copy.deepcopy(model.state_dict())
                patience_counter = 0
            else:
                patience_counter += 1
                if patience_counter >= args.patience:
                    print(f"Early stopping triggered at epoch {epoch}")
                    break
                    
        # Load best weights
        model.load_state_dict(best_model_wts)
        trained_models[model_name] = model
        histories[model_name] = history
        
        # Save model checkpoint
        safe_name = model_name.lower().replace('-', '_')
        torch.save(best_model_wts, save_dir / f"best_{safe_name}.pth")
        print(f"Model saved: {save_dir / f'best_{safe_name}.pth'}")
        
    # ── Generate Training Loss Curves ──
    fig, axes = plt.subplots(2, 3, figsize=(16, 10))
    axes = axes.flatten()
    colors = ['#1f77b4', '#ff7f0e', '#2ca02c', '#d62728', '#9467bd']
    
    for idx, model_name in enumerate(models_to_train):
        ax = axes[idx]
        hist = histories[model_name]
        epochs = range(1, len(hist['train_loss']) + 1)
        ax.plot(epochs, hist['train_loss'], color=colors[idx], linewidth=2, label='Train Loss')
        ax.plot(epochs, hist['val_loss'], color=colors[idx], linewidth=2, linestyle='--', label='Val Loss')
        ax.set_title(model_name, fontsize=12, fontweight='bold')
        ax.set_xlabel('Epoch')
        ax.set_ylabel('Loss')
        ax.legend()
        ax.grid(True, alpha=0.3)
        
    # Clear empty subplot
    axes[5].axis('off')
    plt.suptitle("Training & Validation Loss Curves (Stage 1 Binary Classification)", fontsize=15, fontweight='bold', y=1.01)
    plt.tight_layout()
    plt.savefig(save_dir / 'loss_curves.png', dpi=300, bbox_inches='tight')
    plt.close()
    
    # ── Evaluation Utility ──
    def get_predictions(model, loader):
        model.eval()
        all_probs = []
        all_labels = []
        with torch.no_grad():
            for images, labels in loader:
                images = images.to(device)
                with autocast(enabled=True):
                    outputs = model(images)
                    probs = torch.softmax(outputs, dim=-1)[:, 1]
                all_probs.extend(probs.cpu().numpy())
                all_labels.extend(labels.numpy())
        return np.array(all_probs), np.array(all_labels)
        
    # Get validation probabilities for clinical threshold sweep
    val_probs_dict = {}
    val_labels = None
    
    print("\nEvaluating individual models on Validation Set...")
    for model_name, model in trained_models.items():
        probs, labels = get_predictions(model, val_loader)
        val_probs_dict[model_name] = probs
        val_labels = labels
        
    # Compute Ensemble validation probabilities (arithmetic mean)
    val_ens_probs = np.stack(list(val_probs_dict.values()), axis=0).mean(axis=0)
    
    # Sweep clinical thresholds on validation set
    thresholds = np.arange(0.01, 0.99, 0.01)
    clinical_threshold = 0.50
    best_spec = 0.0
    
    for t in thresholds:
        preds = (val_ens_probs >= t).astype(int)
        sens = recall_score(val_labels, preds, pos_label=1, zero_division=0)
        spec = recall_score(val_labels, preds, pos_label=0, zero_division=0)
        
        # Check sensitivity constraint
        if sens >= 0.90:
            if spec > best_spec:
                best_spec = spec
                clinical_threshold = t
                
    print(f"\nClinical Threshold Sweep Result:")
    print(f"  Selected threshold: {clinical_threshold:.2f} (achieves validation Sensitivity >= 0.90)")
    
    # Get test probabilities
    test_probs_dict = {}
    test_labels = None
    
    print("\nEvaluating individual models on Test Set...")
    for model_name, model in trained_models.items():
        probs, labels = get_predictions(model, test_loader)
        test_probs_dict[model_name] = probs
        test_labels = labels
        
        # Log individual test AUC for dynamic targeted Grad-CAM selection
        auc = roc_auc_score(labels, probs)
        print(f"  {model_name} Test AUC: {auc:.4f}")
        
    # Ensemble soft probabilities (arithmetic mean)
    test_ens_probs = np.stack(list(test_probs_dict.values()), axis=0).mean(axis=0)
    
    # ── Test Set Metrics Evaluation ──
    def compute_metrics(probs, labels, threshold):
        preds = (probs >= threshold).astype(int)
        acc = accuracy_score(labels, preds)
        prec = precision_score(labels, preds, zero_division=0)
        rec_mal = recall_score(labels, preds, pos_label=1, zero_division=0) # Sensitivity
        rec_ben = recall_score(labels, preds, pos_label=0, zero_division=0) # Specificity
        f1 = f1_score(labels, preds, zero_division=0)
        auc = roc_auc_score(labels, probs)
        return {
            'Accuracy': acc,
            'Precision': prec,
            'F1-Score': f1,
            'AUC': auc,
            'Sensitivity': rec_mal,
            'Specificity': rec_ben
        }
        
    metrics_default = compute_metrics(test_ens_probs, test_labels, 0.50)
    metrics_clinical = compute_metrics(test_ens_probs, test_labels, clinical_threshold)
    
    print("\n==========================================")
    print(" FINAL ENSEMBLE TEST SET RESULTS")
    print("==========================================")
    print(f"  {'Metric':<15} | {'Default (0.50)':<15} | {f'Clinical ({clinical_threshold:.2f})':<15}")
    print("-" * 55)
    for key in metrics_default:
        print(f"  {key:<15} | {metrics_default[key]:<15.4f} | {metrics_clinical[key]:<15.4f}")
    print("==========================================")
    
    # Save metrics to text report
    with open(save_dir / 'metrics_summary.txt', 'w') as f:
        f.write("=== FINAL ENSEMBLE TEST SET RESULTS ===\n")
        f.write(f"  {'Metric':<15} | {'Default (0.50)':<15} | {f'Clinical ({clinical_threshold:.2f})':<15}\n")
        f.write("-" * 55 + "\n")
        for key in metrics_default:
            f.write(f"  {key:<15} | {metrics_default[key]:<15.4f} | {metrics_clinical[key]:<15.4f}\n")
            
    # ── Confusion Matrices ──
    fig, axes = plt.subplots(1, 2, figsize=(12, 5))
    
    for idx, (thresh, title, m_dict) in enumerate([
        (0.50, "Ensemble @ Default (0.50)", metrics_default),
        (clinical_threshold, f"Ensemble @ Clinical ({clinical_threshold:.2f})", metrics_clinical)
    ]):
        preds = (test_ens_probs >= thresh).astype(int)
        cm = confusion_matrix(test_labels, preds)
        sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', ax=axes[idx],
                    xticklabels=['Benign', 'Malignant'], yticklabels=['Benign', 'Malignant'],
                    cbar=False, annot_kws={"size": 14, "weight": "bold"})
        axes[idx].set_title(f"{title}\nSens: {m_dict['Sensitivity']:.3f} | Spec: {m_dict['Specificity']:.3f}", fontsize=12, fontweight='bold')
        axes[idx].set_xlabel('Predicted Label')
        axes[idx].set_ylabel('True Label')
        
    plt.suptitle("Final Ensemble Confusion Matrices on Test Set", fontsize=14, fontweight='bold')
    plt.tight_layout()
    plt.savefig(save_dir / 'confusion_matrices.png', dpi=300, bbox_inches='tight')
    plt.close()
    
    # ── ROC Curve ──
    plt.figure(figsize=(7, 6))
    fpr, tpr, _ = roc_curve(test_labels, test_ens_probs)
    plt.plot(fpr, tpr, color='#e74c3c', linewidth=2.5, label=f"Ensemble (AUC = {metrics_default['AUC']:.4f})")
    plt.plot([0, 1], [0, 1], 'k--', alpha=0.5)
    
    # Plot default operating point
    fpr_def = 1 - metrics_default['Specificity']
    tpr_def = metrics_default['Sensitivity']
    plt.scatter(fpr_def, tpr_def, color='blue', s=100, zorder=5, label=f"Default (Thresh=0.50)")
    
    # Plot clinical operating point
    fpr_clin = 1 - metrics_clinical['Specificity']
    tpr_clin = metrics_clinical['Sensitivity']
    plt.scatter(fpr_clin, tpr_clin, color='green', s=100, zorder=5, label=f"Clinical (Thresh={clinical_threshold:.2f})")
    
    plt.xlim([-0.01, 1.01])
    plt.ylim([-0.01, 1.01])
    plt.xlabel('False Positive Rate (1 - Specificity)', fontsize=11)
    plt.ylabel('True Positive Rate (Sensitivity)', fontsize=11)
    plt.title('ROC Curve of Final Soft-Voting Ensemble on Test Set', fontsize=12, fontweight='bold')
    plt.legend(loc='lower right', fontsize=10)
    plt.grid(True, alpha=0.3)
    plt.tight_layout()
    plt.savefig(save_dir / 'roc_curve.png', dpi=300, bbox_inches='tight')
    plt.close()
    
    # ── Targeted Grad-CAM ──
    # Dynamically select the single top CNN model based on individual validation AUC
    print("\nDynamic CNN Model Selection for Targeted Grad-CAM...")
    cnn_names = ['ResNet50', 'EfficientNet-B0', 'MobileNetV2', 'ConvNeXt-Tiny']
    best_cnn_name = None
    best_cnn_auc = 0.0
    
    for cnn in cnn_names:
        # Calculate Validation AUC
        auc = roc_auc_score(val_labels, val_probs_dict[cnn])
        print(f"  {cnn} Validation AUC: {auc:.4f}")
        if auc > best_cnn_auc:
            best_cnn_auc = auc
            best_cnn_name = cnn
            
    print(f"Top CNN Selected: {best_cnn_name} (Validation AUC = {best_cnn_auc:.4f})")
    
    top_model = trained_models[best_cnn_name]
    target_layer = get_gradcam_layer(best_cnn_name, top_model)
    
